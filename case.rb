
fav_color = 'green'

case fav_color

when (fav_color == 'red')
    puts "Red like fire!"
when (fav_color == 'orange')
    puts "Orange like, well... an orange."
when (fav_color == 'yellow')
    puts "Yellow daffodils are so pretty in the spring!"
when (fav_color == 'green')
    puts "Have you been to the Emerald City in Oz?"
when (fav_color == 'blue')
    puts "Blue like the sky!"
when (fav_color == 'purple')
    puts "Purple plums are the tastiest."
else
    puts "Hmm, well I don't know what that color is!"
end


today_weather = 'sunny'

case today_weather
when (today_weather == 'cloudy')
    puts "Storms rolling in!"
when (today_weather == 'sunny')
    puts "Lets enjoy some outside time."
when (today_weather == 'snow')
    "Good golly, it is freezing!"
end